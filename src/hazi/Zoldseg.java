package hazi;

public final class Zoldseg {
	private String name;
	private int ar;

	public Zoldseg(String name, int ar) {
		this.name = name;
		this.ar = ar;
	}

	public int getAr() {
		return ar;
	}

	public void setAr(int ar) {
		this.ar = ar;
	}

	public String getName() {
		return name;
	}
	
	

}
